// export const fetchProductData = async (userId, currentPage)
export const fetchProductData = async (userId, currentPageProd, pageSize) => {
  try {
    const response = await axios.get(`https://halaltravel.ai/ht/api/product/${userId}`,
      {
        headers: {
          Authorization: `Bearer ${token}`
        },
        params: {
          pageNumber: currentPageProd,
          pageSize: pageSize,
        }
      })

    console.log("response product data: ", response.data)
    console.log("response product data: ", response.data.totalElements)

    const productContent = response.data.elements.map((elements) => ({
      productId: elements.product.id,
      productCode: elements.product.code,
      productName: elements.product.name,
      productLink: elements.product.link,
      productImage: elements.product.image,
      productPrice: elements.product.price,
      productCurrency: elements.product.currency,
      productDesc: elements.product.description,
      productPin: elements.pinStatus,
      category: 'product',
    }));

    console.log("productContent: ", productContent)
    // return productContent;
    return {
      productContent,
      totalPages: response.data.totalPages
    };

  } catch (error) {
    console.error('Error fetching product data:', error);
    return null;
  }
};


